def translate(seq):
    prot = []
    for i, codon in enumerate(zip(seq[0::3], seq[1::3], seq[2::3])):
        try:
            prot.append(universal_code[''.join(codon)])
        except KeyError:
            # Same as gffread, just use 'X' (codons.cpp-codonTableInit:59)
            prot.append('X')
    prot = "".join(prot)
    return prot
